
## Procedure to print two overlapping brick rows:

## Note that a pair of parentheses, (), must follow the function
## name, and that all statements in the function suite must have
## the same indentation; here, it is 4 spaces.

def print_bricks():
    print("--+-----+---")
    print("|     |")
    print("-----+-----+")
    print("|     |")

## Repeat the two rows three times to make a higher wall:

## Also when a function is called must the name be followed by a
## pair of parentheses.

print_bricks()
print_bricks()
print_bricks()
